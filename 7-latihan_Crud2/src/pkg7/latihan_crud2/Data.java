    private void dataTabel() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NPM");
        model.addColumn("Nama Mahasiswa");
        model.addColumn("Kelas");
        model.addColumn("Jelanis Kelamin");
        model.addColumn("Alamat");
        tabelData.setModel(model);
        try{
            Statement statement = (Statement) Conn.getConnection().createStatement();
            ResultSet result = statement.executeQuery("SELECT * FROM t_mahasiswa");
            while(result.next()){
                model.addRow(new Object[]{
                    result.getString("npm"),
                    result.getString("nama"),
                    result.getString("kelas"),
                    result.getString("jk"),
                    result.getString("alamat"),
                });
                tabelData.setModel(model);
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Koneksi gagal");
        }
    }
