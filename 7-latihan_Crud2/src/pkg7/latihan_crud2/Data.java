    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        try{
            Statement statement = (Statement)Conn.getConnection().createStatement();
            ResultSet result = statement.executeQuery("SELECT * FROM t_mahasiswa WHERE npm LIKE '%"+txtCari.getText()+"%' OR nama LIKE '%"+txtCari.getText()+"%'");
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("NPM");
            model.addColumn("Nama Mahasiswa");
            model.addColumn("Kelas");
            model.addColumn("Jelanis Kelamin");
            model.addColumn("Alamat");
           
            tabelData.setModel(model);
            while(result.next()){
                    model.addRow(new Object[]{
                    result.getString("npm"),
                    result.getString("nama"),
                    result.getString("kelas"),
                    result.getString("jk"),
                    result.getString("alamat"),
                    });
                tabelData.setModel(model);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "error");
        }
    }//GEN-LAST:event_btnCariActionPerformed

    private void tabelDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelDataMouseClicked
        int i = tabelData.getSelectedRow();
        if(i == -1){
            return;
        }
        String npm = (String) tabelData.getValueAt(i, 0);
        txtNpm.setText(npm);
        String nama = (String) tabelData.getValueAt(i, 1);
        txtNama.setText(nama);
        String kelas = (String) tabelData.getValueAt(i, 2);
        comboKelas.setSelectedItem(kelas);
        String jenisKelamin = (String) tabelData.getValueAt(i, 3);
        if(jenisKelamin.equals(radioLaki.getText())){
            radioLaki.setSelected(true);
        }else if(jenisKelamin.equals(radioPerempuan.getText())){
            radioPerempuan.setSelected(true);
        }
        
        String alamat = (String) tabelData.getValueAt(i, 4);
        txtAlamat.setText(alamat);
    }//GEN-LAST:event_tabelDataMouseClicked

