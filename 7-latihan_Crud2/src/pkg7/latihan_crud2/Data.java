    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String npm = txtNpm.getText();
        String nama = txtNama.getText();
        String kelas = comboKelas.getSelectedItem().toString();
        String alamat = txtAlamat.getText();
        String jenisKelamin = null;
        
        if(radioLaki.isSelected()){
            jenisKelamin = radioLaki.getText();
        }else if(radioPerempuan.isSelected()){
            jenisKelamin = radioPerempuan.getText();
        }
        
        try{
            Statement statement = (Statement)Conn.getConnection().createStatement();
            statement.executeUpdate("INSERT INTO t_mahasiswa VALUES ('"+ npm +"','"+ nama +"','"+ kelas +"','"+ jenisKelamin +"','"+ alamat +"')");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            clear();
        }catch(Exception t){
            JOptionPane.showMessageDialog(null, "Data gagal disimpan");
        }
        
        dataTabel();
                
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if(btnEdit.getText() == "btnEdit"){
            txtNpm.getFont();
        }else{
            String npm = txtNpm.getText();
            String nama = txtNama.getText();
            String kelas = comboKelas.getSelectedItem().toString();
            String jenisKelamin = null;
        
            if(radioLaki.isSelected()){
                jenisKelamin = radioLaki.getText();
            }else if(radioPerempuan.isSelected()){
                jenisKelamin = radioPerempuan.getText();
            }
            
            String alamat = txtAlamat.getText();
            
            try{
               Statement statement = (Statement)Conn.getConnection().createStatement();
               statement.executeUpdate("UPDATE t_mahasiswa SET npm='"+ npm +"',nama='"+ nama +"',kelas='"+ kelas +"',jk='"+ jenisKelamin +"',alamat='"+ alamat +"' WHERE npm='"+ npm +"'"); 
               statement.close();
               JOptionPane.showMessageDialog(null, "Data berhasil diubah");
               clear();
            }catch(Exception t){
                JOptionPane.showMessageDialog(null, "Data gagal diubah");
            }
            dataTabel();
        }
    }//GEN-LAST:event_btnEditActionPerformed

