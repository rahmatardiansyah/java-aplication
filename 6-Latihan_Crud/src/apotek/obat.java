    private void reset(){
        field_kodeObat.setText("");
        field_namaObat.setText("");
        field_idKategori.setText("");
        field_kodeSatuan.setText("");
    }
    private void tampilkan() {
        int row = tabel.getRowCount();
        for(int a = 0;a < row;a++){
            model.removeRow(row);
        }
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/apotek", "rahmat", "rahmat21");
            ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM obat");
            while(rs.next()){
                String data[] = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(obat.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
