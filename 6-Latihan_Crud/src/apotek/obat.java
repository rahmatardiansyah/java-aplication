    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int i = tabel.getSelectedRow();
        
        if(i>-1){
            field_kodeObat.setText(model.getValueAt(i, 0).toString());
            field_namaObat.setText(model.getValueAt(i, 1).toString());
            field_idKategori.setText(model.getValueAt(i, 2).toString());
            field_kodeSatuan.setText(model.getValueAt(i, 3).toString());
        }
    }//GEN-LAST:event_tabelMouseClicked

